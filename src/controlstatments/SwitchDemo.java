package controlstatments;

public class SwitchDemo {

    public static void main(String[] args) {
        int num=23;

        switch (num){
            case 1 :
                System.out.println("One");
                break;
            case 2 :
                System.out.println("Two");
                break;
            case 3:
                System.out.println("Three");

            default:
                System.out.println("No found");

                //test push
        }
    }
}
